# Conventional approach
color = ["yellow", "black", "red", "orange", "white"]
yellow = color[0]
black = color[1]
red = color[2]
orange = color[3]
white = color[4]

print(yellow)
print(black)
print(red)
print(orange)
print(white, "\n")

# Pythonic approach
yellow, black, red, orange, white = color
print(yellow)
print(black)
print(red)
print(orange)
print(white)
